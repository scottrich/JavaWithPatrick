
public interface IPalindromeDetector {

	/**
	 * Interface for classes that can test whether a String is a palindrome.
	 * A palindrome reads the same forward and backward.
	 * An empty or null string is not a palindrome.
	 * 
	 * @param palindrome
	 * @return true if the passed string is a palindrome
	 * 
	 */
	boolean isPalindrome(String palindrome);

}